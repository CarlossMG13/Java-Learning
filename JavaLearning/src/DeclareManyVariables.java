public class DeclareManyVariables {
    public static void main(String[] args) {
        // para declarar mas de una variable del mismo type
        int x = 5, y = 6, z = 50;
        System.out.println(x + y + z);

        // asignar un valor a mas de una variable
        int a, b, c;
        a = b = c = 50;
        System.out.println(a + b + c);
    }
}
