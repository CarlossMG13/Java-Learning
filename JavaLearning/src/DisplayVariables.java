public class DisplayVariables {
    public static void main(String[] args) {
        //println() method para mostrar a pantalla
        String name = "Jonh";
        System.out.println("Hello " + name);

        //+ para sumar variables
        String fistName = "Jonh ";
        String lastName = "Doe";
        String fullName = fistName + lastName;
        System.out.println(fullName);

        //como operador matematico
        int x = 5;
        int y = 6;
        System.out.println(x + y); // Imprime el valor de x + y
    }
}
